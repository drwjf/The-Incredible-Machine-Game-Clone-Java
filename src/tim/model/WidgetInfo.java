package tim.model;

/**
 * The base class for information pertinent to the initialization of
 * various Widgets. To be succinct, class stores info on Widgets.
 * @author Nathaniel Ridder
 */
public abstract class WidgetInfo {

	String widget_name;
	
	/**
	 * Retrieves the Widget Class name. Required to initialize
	 * the correct Widget, if we do not know before-hand which
	 * ones will exist.
	 * @return The Widget's class name.
	 */
	public String getWidgetClassName ()
	{
		return widget_name;
	}
}